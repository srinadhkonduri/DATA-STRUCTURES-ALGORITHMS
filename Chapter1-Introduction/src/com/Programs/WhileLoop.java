package com.Programs;

public class WhileLoop {
    public static void main(String[] args) {
        int i = 1;
        while (i <= 9){
            System.out.println(i);
            i++;
        }
        System.out.println("done");

//        for loop
        for (int j = 0; j <= 9; j++) {
            System.out.println(j);
        }
    }
}
